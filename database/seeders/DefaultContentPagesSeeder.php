    private function syncPageMenu(Menu $menu, array $items, array $pagesBySlug): void
    {
        $this->deleteKnownMenuItems($menu);

        $position = 1;

        foreach ($items as $slug => $title) {
            $page = $pagesBySlug[$slug] ?? ContentPage::query()->where('slug', $slug)->first();

            if (! $page) {
                continue;
            }

            MenuItem::query()->updateOrCreate(
                [
                    'menu_id' => $menu->id,
                    'type' => 'page',
                    'linkable_type' => ContentPage::class,
                    'linkable_id' => $page->id,
                ],
                $this->menuItemPayload($title, $position),
            );

            $position++;
        }
    }

    private function syncFooterMenu(Menu $menu, array $pageItems, array $pagesBySlug): void
    {
        $this->deleteKnownMenuItems($menu);

        MenuItem::query()->updateOrCreate(
            [
                'menu_id' => $menu->id,
                'type' => 'custom_url',
                'url' => '/login',
            ],
            [
                ...$this->menuItemPayload('Вхід до кабінету', 1),
                'url' => '/login',
            ],
        );

        $position = 2;

        foreach ($pageItems as $slug => $title) {
            $page = $pagesBySlug[$slug] ?? ContentPage::query()->where('slug', $slug)->first();

            if (! $page) {
                continue;
            }

            MenuItem::query()->updateOrCreate(
                [
                    'menu_id' => $menu->id,
                    'type' => 'page',
                    'linkable_type' => ContentPage::class,
                    'linkable_id' => $page->id,
                ],
                $this->menuItemPayload($title, $position),
            );

            $position++;
        }
    }

    private function deleteKnownMenuItems(Menu $menu): void
    {
        MenuItem::query()
            ->where('menu_id', $menu->id)
            ->where(fn ($query) => $query
                ->where('type', 'page')
                ->orWhereIn('title', [
                    'Наші секрети',
                    'Вхід до кабінету',
                    'Про нас',
                    'Оплата і доставка',
                    'Обмін та повернення',
                    'Контакти',
                    'Угода користувача',
                    'Угода користувача (Оферта)',
                    'Політика конфіденційності',
                    'Відгуки про магазин',
                    'Безкоштовне повернення',
                ]))
            ->delete();
    }

    private function menuItemPayload(string $title, int $position): array
    {
        return [
            'parent_id' => null,
            'title' => $title,
            'url' => null,
            'target' => '_self',
            'icon' => null,
            'badge' => null,
            'css_class' => null,
            'is_active' => true,
            'sort_order' => $position * 10,
            'settings' => null,
        ];
    }
